	public bool CheckRateAppDialog()
	{
		if (TBFUtils.Is256mbDevice() || SwrveServerVariables.Instance.RatingReward <= 0)
		{
			return false;
		}
		SecureStorage instance = SecureStorage.Instance;
		if (instance.LastRatedVersion == TBFUtils.BundleVersion)
		{
			TBFUtils.DebugLog("ALREADY RATED: " + instance.LastRatedVersion + ", " + TBFUtils.BundleVersion);
			return false;
		}
		int timesRatingChecked = instance.TimesRatingChecked;
		timesRatingChecked = (instance.TimesRatingChecked = timesRatingChecked + 1);
		TBFUtils.DebugLog("TIMES CHECKED: " + timesRatingChecked);
		int num2 = instance.CheckedBetweenRaing;
		if (num2 == 0 || instance.LastCheckedVersion != TBFUtils.BundleVersion)
		{
			instance.CheckedBetweenRaing = SwrveServerVariables.Instance.ChecksBetweenRating;
			num2 = SwrveServerVariables.Instance.ChecksBetweenRating;
		}
		TBFUtils.DebugLog("CHECKED COUNT: " + num2);
		instance.LastCheckedVersion = TBFUtils.BundleVersion;
		if (timesRatingChecked < num2)
		{
			return false;
		}
		if (!HasEnoughTimePassed(3))
		{
			return false;
		}
		StartCoroutine("SpawnRateAppDialog");
		return true;
	}

