	private bool LaunchInterstitial()
	{
		if (m_interstitials.Count == 0 || TBFUtils.Is256mbDevice())
		{
			return false;
		}
		SecureStorage instance = SecureStorage.Instance;
		InterstitialAdEntry interstitialAdEntry = null;
		float num = float.MinValue;
		int num2 = int.MaxValue;
		for (int i = 0; i < m_interstitials.Count; i++)
		{
			InterstitialAdEntry interstitialAdEntry2 = m_interstitials[i];
			float interstitialAdvertWeight = instance.GetInterstitialAdvertWeight(interstitialAdEntry2.m_name);
			int interstitialAdvertViewedCount = instance.GetInterstitialAdvertViewedCount(interstitialAdEntry2.m_name);
			interstitialAdvertWeight += interstitialAdEntry2.m_frequency;
			if (interstitialAdvertWeight > 1f)
			{
				bool flag = false;
				if (interstitialAdvertWeight > num)
				{
					flag = true;
				}
				else if (interstitialAdvertWeight == num)
				{
					flag = interstitialAdvertViewedCount < num2;
				}
				if (flag)
				{
					interstitialAdEntry = interstitialAdEntry2;
					num = interstitialAdvertWeight;
					num2 = interstitialAdvertViewedCount;
				}
			}
			instance.SetInterstitialAdvertWeight(interstitialAdEntry2.m_name, interstitialAdvertWeight);
		}
		if (!instance.HasInterstitialCountdown)
		{
			instance.InterstitialCountdown = m_intervalBetweenInterstitials;
		}
		if (--instance.InterstitialCountdown > 0)
		{
			return false;
		}
		if (interstitialAdEntry == null)
		{
			return false;
		}
		if (Application.internetReachability == NetworkReachability.NotReachable)
		{
			return false;
		}
		instance.InterstitialCountdown = m_intervalBetweenInterstitials;
		instance.SetInterstitialAdvertWeight(interstitialAdEntry.m_name, 0f);
		instance.IncIntersitialAdvertViewedCount(interstitialAdEntry.m_name);
		TBFUtils.DebugLog("INTERSTITIAL: " + interstitialAdEntry.m_name);
		TBFUtils.ShowInterstitialAd(interstitialAdEntry.m_url, "pitfall");
		return true;
	}

	public void UpdateFromBedrock()
	{
		List<InterstitialAdEntry> list = new List<InterstitialAdEntry>();
		int num = 10;
		for (int i = 0; i < num; i++)
		{
			string itemId = string.Format("interstitial{0}", i);
			Dictionary<string, string> resourceDictionary = null;
			if (Bedrock.GetRemoteUserResources(itemId, out resourceDictionary))
			{
				string fromResourceDictionaryAsString = Bedrock.GetFromResourceDictionaryAsString(resourceDictionary, "name", string.Empty);
				string fromResourceDictionaryAsString2 = Bedrock.GetFromResourceDictionaryAsString(resourceDictionary, "url", string.Empty);
				float fromResourceDictionaryAsFloat = Bedrock.GetFromResourceDictionaryAsFloat(resourceDictionary, "weight", -1f);
				if (!string.IsNullOrEmpty(fromResourceDictionaryAsString) && !string.IsNullOrEmpty(fromResourceDictionaryAsString2) && fromResourceDictionaryAsFloat > 0f)
				{
					TBFUtils.DebugLog("Read interstitial: " + fromResourceDictionaryAsString);
					InterstitialAdEntry interstitialAdEntry = new InterstitialAdEntry();
					interstitialAdEntry.m_name = fromResourceDictionaryAsString;
					interstitialAdEntry.m_url = fromResourceDictionaryAsString2;
					interstitialAdEntry.m_frequency = fromResourceDictionaryAsFloat;
					list.Add(interstitialAdEntry);
				}
			}
		}
		m_intervalBetweenInterstitials = Bedrock.GetRemoteVariableAsInt("interstitialCountdown", m_intervalBetweenInterstitials);
		TBFUtils.DebugLog("SWRVE - interval between interstitials: " + m_intervalBetweenInterstitials);
		m_interstitials = list;
		TBFUtils.DebugLog("SWRVE - received " + m_interstitials.Count + " intersitials");
	}
