		g_bAwaitingBillingCallback = false;
	}

	private void ConsumeProductIfOwned(string bundleId)
	{
		if (mPurchases == null || mPurchases.Count <= 0)
		{
			return;
		}
		int i = 0;
		for (int count = mPurchases.Count; i < count; i++)
		{
			if (mPurchases[i].productId == bundleId)
			{
				GoogleIAB.consumeProduct(bundleId);
				break;
			}
		}
	}

	private void FixIABv3Issues()
	{
		if (PlayerPrefs.GetInt("NeedsToConsumeBundle1", 1) == 1)
		{
			ConsumeProductIfOwned(Bundle1Indentifier);
			PlayerPrefs.SetInt("NeedsToConsumeBundle1", 0);
			PlayerPrefs.Save();
		}
		else if (PlayerPrefs.GetInt("NeedsToConsumeBundle2", 1) == 1)
		{
			ConsumeProductIfOwned(Bundle2Indentifier);
			PlayerPrefs.SetInt("NeedsToConsumeBundle2", 0);
			PlayerPrefs.Save();
		}
		else if (PlayerPrefs.GetInt("NeedsToConsumeBundle3", 1) == 1)
		{
			ConsumeProductIfOwned(Bundle3Indentifier);
			PlayerPrefs.SetInt("NeedsToConsumeBundle3", 0);
			PlayerPrefs.Save();
		}
		else if (PlayerPrefs.GetInt("NeedsToConsumeBundle4", 1) == 1)
		{
			ConsumeProductIfOwned(Bundle4Indentifier);
			PlayerPrefs.SetInt("NeedsToConsumeBundle4", 0);
			PlayerPrefs.Save();
		}
	}
