	private void UpdateSalesFromBedrock()
	{
		m_saleItem = null;
		Dictionary<string, string> resourceDictionary = null;
		if (!Bedrock.GetRemoteUserResources("itemForSale", out resourceDictionary))
		{
			return;
		}
		string fromResourceDictionaryAsString = Bedrock.GetFromResourceDictionaryAsString(resourceDictionary, "item", string.Empty);
		if (string.IsNullOrEmpty(fromResourceDictionaryAsString))
		{
			return;
		}
		BaseCampProducts.ProductData productData = FindProduct(fromResourceDictionaryAsString);
		if (productData != null)
		{
			m_originalSaleItem = new BaseCampProducts.ProductData();
			productData.CopyTo(m_originalSaleItem);
			m_saleItem = productData;
			m_saleItem.UpdateFromBedrock(resourceDictionary);
			string fromResourceDictionaryAsString2 = Bedrock.GetFromResourceDictionaryAsString(resourceDictionary, "startDate", string.Empty);
			if (!uint.TryParse(fromResourceDictionaryAsString2, out m_saleStart))
			{
				m_saleStart = 0u;
			}
			string fromResourceDictionaryAsString3 = Bedrock.GetFromResourceDictionaryAsString(resourceDictionary, "duration", string.Empty);
			if (!uint.TryParse(fromResourceDictionaryAsString3, out m_saleDuration))
			{
				m_saleDuration = 0u;
			}
		}
	}

