	private IEnumerator VerifyReceipt(string json)
	{
		Hashtable hashed = json.hashtableFromJson();
		if (Application.internetReachability == NetworkReachability.NotReachable)
		{
			yield return null;
			yield break;
		}
		string serverURL = GetReceiptVerificationURL();
		string encodedtext = EncodeStringBase64(json);
		string postFields = string.Format("receipt={0}", encodedtext);
		ASCIIEncoding encoding = new ASCIIEncoding();
		byte[] postData = encoding.GetBytes(postFields);
		Request verifyReq = new Request("post", serverURL, postData);
		verifyReq.AddHeader("content-type", "application/x-www-form-urlencoded");
		verifyReq.Send();
		while (!verifyReq.isDone)
		{
			yield return null;
		}
		if (verifyReq.response != null && verifyReq.response.status == 200)
		{
			StoreLog("Encoded: " + verifyReq.response.Text);
			string decodedText = DecodeStringBase64(verifyReq.response.Text);
			StoreLog("Decoded: " + decodedText);
			if (decodedText.Equals(GamePurchasedItem))
			{
				SwrveEventsPurchase.ReceiptVerifySuccess(GamePurchasedItem);
				SecureStorage.Instance.HasFailedReceiptVerification = false;
			}
			else
			{
				SwrveEventsPurchase.ReceiptVerifyFailed(GamePurchasedItem, 0);
				SecureStorage.Instance.HasFailedReceiptVerification = true;
			}
		}
		else
		{
			StoreLog("ERROR: Invalid response received from verification server");
			SwrveEventsPurchase.ReceiptVerifyServerError();
		}
	}

